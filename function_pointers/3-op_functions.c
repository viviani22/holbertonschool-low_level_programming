#include "3-calc.h"
/**
 * op_add - called from pointer
 * @a: int
 * @b: int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - called from pointer
 * @a: int
 * @b: int
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - called from pointer
 * @a: int
 * @b: int
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - called from pointer
 * @a: int
 * @b: int
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - called from pointer
 * @a: int
 * @b: int
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
